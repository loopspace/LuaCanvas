colour = class()

local svg,x11 = {},{}

function colour:init(r,g,b,a)
   if type(r) == "string" then
      if r:sub(1,1) == '#' then
	 if r:len() == 7 then
	    b = tonumber(r:sub(6,7),16)
	    g = tonumber(r:sub(4,5),16)
	    r = tonumber(r:sub(2,3),16)
	 elseif r:len() == 4 then
	    b = tonumber(r:sub(4,4),16)
	    g = tonumber(r:sub(3,3),16)
	    r = tonumber(r:sub(2,2),16)
	 elseif r:len() == 3 then
	    b = tonumber(r:sub(2,3),16)
	    g = tonumber(r:sub(2,3),16)
	    r = tonumber(r:sub(2,3),16)
	 elseif r:len() == 2 then
	    b = tonumber(r:sub(2,2),16)
	    g = tonumber(r:sub(2,2),16)
	    r = tonumber(r:sub(2,2),16)
	 end
	 a = 255
      elseif r:sub(1,3) == 'rgb' then
	 local css = {}
	 for c in r:gfind('%d+') do
	    table.insert(css,c)
	 end
	 r = c[1]
	 g = c[2]
	 b = c[3]
	 a = c[4] or 255
      elseif svg[r] then
	 r,g,b,a = unpack(svg[r])
      elseif x11[r] then
	 r,g,b,a = unpack(x11[r])
      end
   elseif type(r) == "table" and r:is_a(colour) then
      -- r is an instance of colour so clone it
      a = r.a
      b = r.b
      g = r.g
      r = r.r
   elseif type(g) == "undefined" then
      -- one argument, so grey
      r = r
      g = r
      b = r
      a = 255
   elseif type(b) == "undefined" then
      -- two arguments, so grey with alpha
      r = r
      g = r
      b = r
      a = g
   elseif type(a) == "undefined" then
      -- three arguments, so colour
      r = r
      g = g
      b = b
      a = 255
   else
      r = r
      g = g
      b = b
      a = a
   end
   self.r = r or 255
   self.g = g or r or 255
   self.b = b or r or 255
   self.a = a or 255
end

function colour:__tostring()
   return string.format("(%d,%d,%d,%d)",self.r,self.g,self.b,self.a)
end

function colour:toCSS()
   local a=self.a/255
   return string.format("rgba(%d,%d,%d,%f)",self.r,self.g,self.b,a)
end

function colour:toHex()
   return string.format("#%02x%02x%02x",self.r,self.g,self.b)
end

function colour:mix(c,t)
   local r,g,b,a,s
   s = 1 - r
   r = t * c.r + s * self.r
   g = t * c.g + s * self.g
   b = t * c.b + s * self.b
   a = t * c.a + s * self.a
   return colour(r,g,b,a)
end

function colour:blend(c)
   local r,g,b,a,s,t
   s = self.a/255
   t = 1 - s
   r = t * c.r + s * self.r
   g = t * c.g + s * self.g
   b = t * c.b + s * self.b
   a = t * c.a + s * self.a
   return colour(r,g,b,a)
end

